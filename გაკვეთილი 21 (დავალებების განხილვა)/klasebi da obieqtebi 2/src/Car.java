public class Car {
    String make;
    String model;
    int year;
    String licensePlate;

    void printInfo() {
        System.out.println("Make: " + make);
        System.out.println("Model: " + model);
        System.out.println("Year: " + year);
        System.out.println("License Plate: " + licensePlate);
    }
}
